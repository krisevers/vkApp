#include "swapchain.h"

VkResult SwapChain::init(Context& context, GLFWwindow* window) {
    createSwapChain(context, window);
    createImageViews(context);

    return VK_SUCCESS;
}

void SwapChain::cleanup(Context& context) {
    vkDestroyImageView(context.device, depthImageView, nullptr);
    vkDestroyImage(context.device, depthImage, nullptr);
    vkFreeMemory(context.device, depthImageMemory, nullptr);
    
    for (size_t i = 0; i < swapChainFramebuffers.size(); i++) {
        vkDestroyFramebuffer(context.device, swapChainFramebuffers[i], nullptr);
    }

    for (auto imageView : swapChainImageViews) {
        vkDestroyImageView(context.device, imageView, nullptr);
    }

    vkDestroySwapchainKHR(context.device, swapChain, nullptr);
}

VkResult SwapChain::recreate(Context& context, GLFWwindow* window, VkRenderPass renderPass) {
    int width = 0, height = 0;
    glfwGetFramebufferSize(window, &width, &height);
    while (width == 0 || height == 0) {
        glfwGetFramebufferSize(window, &width, &height);
        glfwWaitEvents();
    }

    vkDeviceWaitIdle(context.device);
    cleanup(context);
    
    createSwapChain(context, window);
    createImageViews(context);
    createDepthResources(context);
    createFramebuffers(context, renderPass);

    return VK_SUCCESS;
}

